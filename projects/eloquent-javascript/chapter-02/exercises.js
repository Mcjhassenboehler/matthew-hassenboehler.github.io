function drawChessboard(x) {
// create array to store rows of strings
var board = [];
//create a loop to run x amount of times
for (var i = 0; i < x; i++){
  //create row variable
  var row = ""; // "" => " # #"
  // iterate to create row
  for (var j = 0; j < x; j++){
    //determine if we add space or add hashtag
    if ((j + i) % 2 === 0){
      row += " ";
    } else {
      row += "#"
    }
  }
  board.push(row);
}
console.log(board.join("\n") + "\n")
}


