// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(x, y) {
  return (Math.min(x, y));
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

const isEven = (x) => x % 2 === 0

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, char) {
  var count = 0;
  for(var i = 0; i < str.length; i++){
    if (str[i] === char){
      count ++
    }
  }
  return count
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {
  var count = 0;
  for(var i = 0; i < str.length; i++){
    if (str[i] === 'B'){
      count ++
    }
  }
  return count
}

