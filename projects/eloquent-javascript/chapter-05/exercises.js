// /////////////////////////////////////////////////////////////////////////////
// flatten /////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function flatten(array) {
  return array.reduce((acc, current) => acc.concat(current), [])
}

// /////////////////////////////////////////////////////////////////////////////
// loop ////////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function loop(value, test, update, body) {
  // test is checking if the loop should keep going
  while (test(value)){
    // body is used to find out the new value for value
    body(value);
    // update changes the value of value and it loops determining if the new value is true
    value = update(value);
  }
}

// /////////////////////////////////////////////////////////////////////////////
// every ///////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function every(array, test) {
    if (array.every(test)){
      return true
    }
  return false
}

// /////////////////////////////////////////////////////////////////////////////
// dominantDirection ///////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function dominantDirection(string) {
  let ltr = [];
  let rtl = [];
  for (let i = 0; i < string.length; i++){
    let script = characterScript(string.charCodeAt(i));
    if (script !== null){
      if (script.direction === 'ltr'){
        ltr.push(script);
      } else {
        rtl.push(script);
      }
    }
  }
  if (ltr.length > rtl.length){
    return 'ltr';
  } else {
    return 'rtl'
  }
}

